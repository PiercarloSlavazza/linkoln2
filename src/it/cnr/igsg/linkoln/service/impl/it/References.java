  /**
   * Resumes scanning until the next regular expression is matched,
   * the end of input is encountered or an I/O-Error occurs.
   *
   * @return      the next token
   * @exception   java.io.IOException  if any I/O-Error occurs
   */
  public int yylex() throws java.io.IOException {
    int zzInput;
    int zzAction;

    // cached fields:
    int zzCurrentPosL;
    int zzMarkedPosL;
    int zzEndReadL = zzEndRead;
    char [] zzBufferL = zzBuffer;
    char [] zzCMapL = ZZ_CMAP;

    int [] zzTransL = ZZ_TRANS;
    int [] zzRowMapL = ZZ_ROWMAP;
    int [] zzAttrL = ZZ_ATTRIBUTE;

    while (true) {
      zzMarkedPosL = zzMarkedPos;

      yychar+= zzMarkedPosL-zzStartRead;

      boolean zzR = false;
      int zzCh;
      int zzCharCount;
      for (zzCurrentPosL = zzStartRead  ;
           zzCurrentPosL < zzMarkedPosL ;
           zzCurrentPosL += zzCharCount ) {
        zzCh = Character.codePointAt(zzBufferL, zzCurrentPosL, zzMarkedPosL);
        zzCharCount = Character.charCount(zzCh);
        switch (zzCh) {
        case '\u000B':  // fall through
        case '\u000C':  // fall through
        case '\u0085':  // fall through
        case '\u2028':  // fall through
        case '\u2029':
          yyline++;
          yycolumn = 0;
          zzR = false;
          break;
        case '\r':
          yyline++;
          yycolumn = 0;
          zzR = true;
          break;
        case '\n':
          if (zzR)
            zzR = false;
          else {
            yyline++;
            yycolumn = 0;
          }
          break;
        default:
          zzR = false;
          yycolumn += zzCharCount;
        }
      }

      if (zzR) {
        // peek one character ahead if it is \n (if we have counted one line too much)
        boolean zzPeek;
        if (zzMarkedPosL < zzEndReadL)
          zzPeek = zzBufferL[zzMarkedPosL] == '\n';
        else if (zzAtEOF)
          zzPeek = false;
        else {
          boolean eof = zzRefill();
          zzEndReadL = zzEndRead;
          zzMarkedPosL = zzMarkedPos;
          zzBufferL = zzBuffer;
          if (eof) 
            zzPeek = false;
          else 
            zzPeek = zzBufferL[zzMarkedPosL] == '\n';
        }
        if (zzPeek) yyline--;
      }
      zzAction = -1;

      zzCurrentPosL = zzCurrentPos = zzStartRead = zzMarkedPosL;
  
      zzState = ZZ_LEXSTATE[zzLexicalState];

      // set up zzAction for empty match case:
      int zzAttributes = zzAttrL[zzState];
      if ( (zzAttributes & 1) == 1 ) {
        zzAction = zzState;
      }


      zzForAction: {
        while (true) {
    
          if (zzCurrentPosL < zzEndReadL) {
            zzInput = Character.codePointAt(zzBufferL, zzCurrentPosL, zzEndReadL);
            zzCurrentPosL += Character.charCount(zzInput);
          }
          else if (zzAtEOF) {
            zzInput = YYEOF;
            break zzForAction;
          }
          else {
            // store back cached positions
            zzCurrentPos  = zzCurrentPosL;
            zzMarkedPos   = zzMarkedPosL;
            boolean eof = zzRefill();
            // get translated positions and possibly new buffer
            zzCurrentPosL  = zzCurrentPos;
            zzMarkedPosL   = zzMarkedPos;
            zzBufferL      = zzBuffer;
            zzEndReadL     = zzEndRead;
            if (eof) {
              zzInput = YYEOF;
              break zzForAction;
            }
            else {
              zzInput = Character.codePointAt(zzBufferL, zzCurrentPosL, zzEndReadL);
              zzCurrentPosL += Character.charCount(zzInput);
            }
          }
          int zzNext = zzTransL[ zzRowMapL[zzState] + zzCMapL[zzInput] ];
          if (zzNext == -1) break zzForAction;
          zzState = zzNext;

          zzAttributes = zzAttrL[zzState];
          if ( (zzAttributes & 1) == 1 ) {
            zzAction = zzState;
            zzMarkedPosL = zzCurrentPosL;
            if ( (zzAttributes & 8) == 8 ) break zzForAction;
          }

        }
      }

      // store back cached position
      zzMarkedPos = zzMarkedPosL;

      if (zzInput == YYEOF && zzStartRead == zzCurrentPos) {
        zzAtEOF = true;
        return YYEOF;
      }
      else {
        switch (zzAction < 0 ? zzAction : ZZ_ACTION[zzAction]) {
          case 1: 
            { addText(yytext());
	position++;
            } 
            // fall through
          case 22: break;
          case 2: 
            { addText(yytext());
		position++;
            } 
            // fall through
          case 23: break;
          case 3: 
            { offset++;
		position++;
		
		//System.out.println("check " + yytext() + " " + offset + " " + position + " " + length);
		
		if(offset >= length) {
			
			if(offset > length) {
				yypushback(1);
				position--;
			}
			
			checkReference();
			
			isCaseLawReference = false;
			isLegislationReference = false;
			isEuropeanReference = false;
			
			yybegin(0);
		}

		buffer.append(yytext());
            } 
            // fall through
          case 24: break;
          case 4: 
            { offset++;
		position++;

		buffer.append(yytext());
            } 
            // fall through
          case 25: break;
          case 5: 
            { addText(yytext());
		position--; //account for the following {LKN_C} character
		yybegin(YYINITIAL);
            } 
            // fall through
          case 26: break;
          case 6: 
            { addText(yytext()); 
	yybegin(lkn);
            } 
            // fall through
          case 27: break;
          case 7: 
            { isCaseLawReference = true;
	isLegislationReference = false;	
	isEuropeanReference = false;
	
	//System.out.println("CL_PATTERN: " + yytext());
	
	startReference();
            } 
            // fall through
          case 28: break;
          case 8: 
            { AnnotationEntity retrievedEntity = retrieveEntity(yytext());
		offset += yylength();
		position += retrievedEntity.getText().length();
		buffer.append(yytext());
            } 
            // fall through
          case 29: break;
          case 9: 
            { readFeature(false);
            } 
            // fall through
          case 30: break;
          case 10: 
            { readFeature(true);
            } 
            // fall through
          case 31: break;
          case 11: 
            { updateAuthority("CL_AUTH");
            } 
            // fall through
          case 32: break;
          case 12: 
            { updateAuthority("LEG_AUTH");
            } 
            // fall through
          case 33: break;
          case 13: 
            { isCaseLawReference = true;
	isLegislationReference = false;	
	isEuropeanReference = true;
	
	//System.out.println("EU_CL_PATTERN: " + yytext());
	
	startReference();
            } 
            // fall through
          case 34: break;
          case 14: 
            { updateAuthority("EU_CL_AUTH");
            } 
            // fall through
          case 35: break;
          case 15: 
            { updateAuthority("EU_LEG_AUTH");
            } 
            // fall through
          case 36: break;
          case 16: 
            { updateAuthority("CL_AUTH_SECTION");
            } 
            // fall through
          case 37: break;
          case 17: 
            { updateAuthority("CL_DETACHED_SECTION");
            } 
            // fall through
          case 38: break;
          case 18: 
            { isCaseLawReference = false;
	isLegislationReference = false;
	isEuropeanReference = false;

	//System.out.println("GEN_PATTERN: " + yytext());
	
	startReference();
            } 
            // fall through
          case 39: break;
          case 19: 
            { isCaseLawReference = false;
	isLegislationReference = true;
	isEuropeanReference = false;

	startReference();
            } 
            // fall through
          case 40: break;
          case 20: 
            { startUpdateAuthority();
            } 
            // fall through
          case 41: break;
          case 21: 
            { isCaseLawReference = false;
	isLegislationReference = true;
	isEuropeanReference = true;

	startReference();
            } 
            // fall through
          case 42: break;
          default:
            zzScanError(ZZ_NO_MATCH);
        }
      }
    }
  }

  /**
   * Runs the scanner on input files.
   *
   * This is a standalone scanner, it will print any unmatched
   * text to System.out unchanged.
   *
   * @param argv   the command line, contains the filenames to run
   *               the scanner on.
   */
  public static void main(String argv[]) {
    if (argv.length == 0) {
      System.out.println("Usage : java References [ --encoding <name> ] <inputfile(s)>");
    }
    else {
      int firstFilePos = 0;
      String encodingName = "UTF-8";
      if (argv[0].equals("--encoding")) {
        firstFilePos = 2;
        encodingName = argv[1];
        try {
          java.nio.charset.Charset.forName(encodingName); // Side-effect: is encodingName valid? 
        } catch (Exception e) {
          System.out.println("Invalid encoding '" + encodingName + "'");
          return;
        }
      }
      for (int i = firstFilePos; i < argv.length; i++) {
        References scanner = null;
        try {
          java.io.FileInputStream stream = new java.io.FileInputStream(argv[i]);
          java.io.Reader reader = new java.io.InputStreamReader(stream, encodingName);
          scanner = new References(reader);
          while ( !scanner.zzAtEOF ) scanner.yylex();
        }
        catch (java.io.FileNotFoundException e) {
          System.out.println("File not found : \""+argv[i]+"\"");
        }
        catch (java.io.IOException e) {
          System.out.println("IO error scanning file \""+argv[i]+"\"");
          System.out.println(e);
        }
        catch (Exception e) {
          System.out.println("Unexpected exception:");
          e.printStackTrace();
        }
      }
    }
  }


