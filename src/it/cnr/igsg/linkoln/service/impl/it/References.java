		if(isCaseLawReference && isEuropeanReference) newAnnotationEntity = new EuropeanCaseLawReference();
		if(isLegislationReference && !isEuropeanReference) newAnnotationEntity = new LegislationReference();
		if(isLegislationReference && isEuropeanReference) newAnnotationEntity = new EuropeanLegislationReference();
		
		references.add(newAnnotationEntity);
		
		newAnnotationEntity.setPosition(position);
		newAnnotationEntity.setContext(((Reference) annotationEntity).getContext());
		
		newAnnotationEntity.name2sharedEntity.putAll(((Reference) annotationEntity).name2sharedEntity);		
		
